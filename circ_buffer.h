#ifndef __CIRC_BUFFER_H__
#define __CIRC_BUFFER_H__

struct circ_buffer {
    const char *name;
    size_t element_size;

    size_t size;
    size_t head;
    size_t tail;

    void *data;
};

typedef int (*buffer_handler)(void *data);

int circ_buffer_init(struct circ_buffer **buffer, const char *name,
        size_t element_size, size_t buffer_size);
void circ_buffer_deinit(struct circ_buffer *buffer);
int circ_buffer_push(struct circ_buffer *buffer, void *data, int num);
int circ_buffer_pop(struct circ_buffer *buffer, void *data, int num);
int circ_buffer_for_each(struct circ_buffer *buffer, buffer_handler handler);

#endif
